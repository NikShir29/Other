import math

# ------------------------------------------------------------
# Задача 1. Размеры и масса молекул
# ------------------------------------------------------------
print("=== ЗАДАЧА 1: РАЗМЕРЫ И МАССА МОЛЕКУЛ ВОДЫ ===\n")

M_g_mol = 18.0               # молярная масса воды, г/моль
rho_kg_m3 = 1000.0           # плотность воды, кг/м³
N_A = 6.02e23                # число Авогадро, моль⁻¹
V_mm3 = 1.0                  # объём капли, мм³

# Перевод в СИ
M = M_g_mol * 1e-3           # кг/моль
V = V_mm3 * 1e-9             # м³  (1 мм³ = 10⁻⁹ м³)

# 1. Масса одной молекулы воды
m0 = M / N_A                 # кг

# 2. Количество молекул в капле воды
#    Сначала масса капли: m_kapli = rho * V
#    Затем N = (m_kapli / M) * N_A = rho * V * N_A / M
N_molekul = (rho_kg_m3 * V / M) * N_A

# 3. Оценка диаметра молекулы воды
#    Объём, приходящийся на одну молекулу в жидкой воде
V0 = m0 / rho_kg_m3         # м³
#    Предполагаем сферическую форму: V0 = (π/6) * d³  =>  d = (6 * V0 / π)^(1/3)
d = (6 * V0 / math.pi) ** (1/3)

# Вывод результатов
print(f"Масса одной молекулы воды m₀ = {m0:.3e} кг")
print(f"Количество молекул в капле объёмом {V_mm3} мм³: N = {N_molekul:.3e}")
print(f"Оценка диаметра молекулы воды d = {d:.3e} м  ({d*1e9:.2f} нм)")

print("\n" + "="*50 + "\n")
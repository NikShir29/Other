# ------------------------------------------------------------
# Задача 2. Давление газа
# ------------------------------------------------------------
print("=== ЗАДАЧА 2: ДАВЛЕНИЕ ГАЗА ===\n")

N_gas = 1000                # количество молекул
v = 500.0                   # средняя скорость молекул, м/с
m0_gas = 4.65e-26           # масса одной молекулы (воздух), кг
S = 1.0                     # площадь стенки, м²
t = 1.0                     # время наблюдения, с

# В данной упрощённой модели считаем, что все молекулы движутся
# перпендикулярно стенке и каждая ударяется о неё ровно один раз за время t.
# Тогда:
# 1. Число ударов о стенку за 1 секунду
udary = N_gas               # все 1000 молекул ударятся за 1 с

# Изменение импульса одной молекулы при упругом ударе о стенку
# (движение перпендикулярно, скорость меняется с +v на -v)
delta_p = 2 * m0_gas * v    # кг·м/с

# 2. Сила, действующая на стенку (импульс, передаваемый в единицу времени)
F = udary * delta_p / t     # Н (кг·м/с²)

# 3. Давление газа
P = F / S                   # Па

# Вывод результатов
print(f"Число ударов молекул о стенку за 1 с: {udary}")
print(f"Сила давления на стенку: F = {F:.3e} Н")
print(f"Давление газа: P = {P:.3e} Па")

print("\nИспользована простейшая модель, в которой все")
print("молекулы за время наблюдения ударяются о стенку ровно один раз.")